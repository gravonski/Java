package TiposDeDados;

public class DadosNaoPrimitivos {
    public static void main(String[] args) {
        /*
        * Objetivo: Criar um NINJA e atribuir métodos a ele;
        * */
        String name = "Naruto Uzumaki";
        String nameUpperCase = name.toUpperCase();
        System.out.println("Eu sou o " + name);
        System.out.println(nameUpperCase + " é meu nome");

        String village = "Aldeia da Folha";
        String villageLowerCase = village.toLowerCase();
        System.out.println(villageLowerCase);
    }
}
